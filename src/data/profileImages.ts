export function getImageSource(photo: any): any {
  if (!photo) return PLACEHOLDER;
  if (typeof photo === 'number') return photo;
  if (typeof photo === 'string') return { uri: photo };
  return photo;
}

export function getProfilePhotos(profileId: string): any[] {
  const raw = PROFILE_IMAGES[profileId];
  if (!raw || raw.length === 0) {
    console.warn(`No images found for profile: ${profileId}`);
    return Array(6).fill(PLACEHOLDER);
  }
  const photos = [...raw];
  while (photos.length < 6) photos.push(photos[photos.length - 1]);
  return photos.slice(0, 6);
}
